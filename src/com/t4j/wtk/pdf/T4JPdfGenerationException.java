
package com.t4j.wtk.pdf;

/**
 * 
 * @author Tenentia-4j, S.L.
 * 
 */
public class T4JPdfGenerationException extends Exception {

	private static final long serialVersionUID = 1L;

	public T4JPdfGenerationException() {

		super();
	}

	public T4JPdfGenerationException(String message) {

		super(message);
	}

	public T4JPdfGenerationException(String message, Throwable cause) {

		super(message, cause);
	}

	public T4JPdfGenerationException(Throwable cause) {

		super(cause);
	}

}
